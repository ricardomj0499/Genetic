public class Cromosomas {
    String cromosoma;
    int color;
    int cordenada;

    public Cromosomas() {
    }

    public String getCromosoma() {
        return cromosoma;
    }

    public void setCromosoma(String cromosoma) {
        this.cromosoma = cromosoma;
    }

    public int getColor() {
        return color;
    }

    public void setColor(int color) {
        this.color = color;
    }

    public int getCordenada() {
        return cordenada;
    }

    public void setCordenada(int cordenada) {
        this.cordenada = cordenada;
    }
    
    
}
